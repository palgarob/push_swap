#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (ft_error_ptr(ERROR_MALLOC, 0, 0));
	new->content = content;
	new->next = NULL;
	return (new);
}
